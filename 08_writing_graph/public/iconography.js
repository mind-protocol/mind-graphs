export function iconForNode(nodeOrType) {
  const type = typeof nodeOrType === "string" ? nodeOrType : (nodeOrType?.semanticType || nodeOrType?.nodeType);
  return NODE_TYPE_ICONS[type] || "•";
}

export function iconForRelation(linkOrType) {
  const type = typeof linkOrType === "string" ? linkOrType : linkOrType?.type;
  return RELATION_TYPE_ICONS[type] || "—";
}
